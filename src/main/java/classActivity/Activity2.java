package classActivity;

public class Activity2 {
    public static void main(String[] args) {

        String city = "Lagos";

        if (city.equals("Ibadan")) {
            System.out.println("City is Ibadan");
        } else if (city.equals("Benin")) {
            System.out.println("City is Benin");
        } else if (city.equals("Enugu")) {
            System.out.println("City is Enugu");
        } else {
            System.out.println("City is Lagos");
        }

    }
}
